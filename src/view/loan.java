    private void jLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jLoanActionPerformed
        // TODO add your handling code here:
        hasilakhir.setVisible(true);

        double AnnualInterestRate = Double.parseDouble(a.getText());
        double MonthlyInterestRate = AnnualInterestRate/1200;
        int NumberOfYears = Integer.parseInt(b.getText());
        double loanAmount = Double.parseDouble(c.getText());
        //================================

        double MonthlyPayment =  loanAmount * MonthlyInterestRate/(1 - 1/Math.pow(1+MonthlyInterestRate,
            NumberOfYears*12));

    String iMonthlyPayment;
    iMonthlyPayment=Double.toString(MonthlyPayment);
    iMonthlyPayment=String.format("Rp%f",MonthlyPayment);
    d.setText(iMonthlyPayment);
    //=================================
    double TotalPayment = MonthlyPayment*NumberOfYears*12;
    String iTotalPayment;
    iTotalPayment = String.format("Rp%f",TotalPayment);
    e.setText(iTotalPayment);
    //-------------------------------

    }//GEN-LAST:event_jLoanActionPerformed

