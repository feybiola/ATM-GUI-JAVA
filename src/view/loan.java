    private void jReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jReceiptActionPerformed
        // TODO add your handling code here:
        String v = String.format(a.getText());
        String w = String.format(b.getText());
        String x = String.format(c.getText());
        String y = String.format(d.getText());
        String z = String.format(e.getText());
        isian.setVisible(true);

        int refs = 1325 + (int) (Math.random()*4238);

        Calendar timer = Calendar.getInstance();
        timer.getTime();
        SimpleDateFormat tTime = new SimpleDateFormat("HH:mm:ss");
        tTime.format(timer.getTime());
        SimpleDateFormat Tdate = new SimpleDateFormat("dd-MMM-yyyy");
        Tdate.format(timer.getTime());

        jTextArea1.append("\tLoan Management System\n" +
            "Reference: \t\t\t" + refs +
            "\n============================================\t"+
            "\nInterest rate:\t\t\t" + v +
            "\nRepayment years:\t\t" + w +
            "\nAmount of Loan:\t\t" +"Rp"+ x +
            "\nMonthly payment:\t\t " + y +
            "\nTotal payment:\t\t\t " + z +

            "\n=============================================\t" +
            "\nDate: " +Tdate.format(timer.getTime()) +
            "\t\tTimer: " + tTime.format(timer.getTime()) +
            "\n\n\tThank You!\n" );

    }//GEN-LAST:event_jReceiptActionPerformed
